# Crie um algoritmo que leia um número e mostre o seu Dobro, Triplo e raiz quadrada. #

a = int(input('Digite um número: '))
dobro = a * 2
triplo = a * 3
raiz = a ** (1/2)
print(f'O Dobro de {a} vale {dobro}')
print(f'O Triplo de {a} vale {triplo}')
print(f'A Raiz Quadrada de {a} é igual a {raiz}')
